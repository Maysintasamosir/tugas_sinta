fun main() {
        print("Masukkan angka pertama: ")
        val angka1 = readLine()!!.toDouble()

        print("Masukkan angka kedua: ")
        val angka2 = readLine()!!.toDouble()

        println("hasil =${angka1 + angka2}")
        println("hasil =${angka1 - angka2}")
        println("hasil =${angka1 * angka2}")
        println("hasil =${angka1 / angka2}")
}